-- FILE 테이블
CREATE TABLE FILE
(
    FILE_ID     BIGINT AUTO_INCREMENT PRIMARY KEY,
    RELATED_ID  BIGINT       NOT NULL,              -- 관련 엔티티 ID (공간, 게시판, 공지사항 등)
    ENTITY_TYPE VARCHAR(50)  NOT NULL,              -- 엔티티 유형 (예: SPACE, BOARD, NOTICE 등)
    FILE_NAME   VARCHAR(255) NOT NULL,              -- 파일 이름
    FILE_SIZE   BIGINT       NOT NULL,              -- 파일 크기
    FILE_PATH   VARCHAR(255) NOT NULL,              -- 파일 경로 (AWS S3 URL 등)
    UPLOAD_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP -- 업로드 시간
);

